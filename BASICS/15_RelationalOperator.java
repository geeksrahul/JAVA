// Relational (comparison)
// return true or false by comparing two values
class RelationalOperator {
    public static void main(String[] args) {
        System.out.println(10 > 5); // true greater than operator
        System.out.println(10 < 5); // false lesser than operator
        System.out.println(10 >= 5); // true greater than or equal to
        System.out.println(10 <= 5); // false lesser than or equal to 
        System.out.println(10 == 5); // false equal to
        System.out.println(10 != 5); // true not equal to
    }
}